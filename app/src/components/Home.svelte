<h1>Introduction</h1>

<p>
    Nous avons abordé dans ce projet un sujet souvent évoqué mais jamais assez : le réchauffement climatique. L’industrialisation massive des sociétés depuis le début du XXème siècle s’accompagne de rejets de gaz à effet de serre en quantités démesurées.
    Cet enjeu nécessite une prise de conscience collective, et nous avons pour cela choisi une stratégie de sensibilisation par la visualisation. L’objectif n’est bien évidemment pas de faire le portrait d’un scénario catastrophe comme cela est souvent fait, mais uniquement de retranscrire le plus objectivement possible les données observées et prédites par les experts.
</p>

<p>
    Cette visualisation s’adresse à toute personne sensible à la question environnementale, mais également aux adeptes du climato-scepticisme à l’image du président américain Donald Trump. 
    Nous essayons a travers ce travail de retranscrire à la fois tendance historique de ce fléau, mais aussi les prévisions faites par les experts en fonctions de différents scénarios. 
</p>

